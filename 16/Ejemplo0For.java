/*
 * programa que muestra los números del 1 al 10
 */
public class Ejemplo0For {
    public static void main(String[] args) {
        int i;
        //Hace que una instrucción  se repita un número determinado de veces mientras se cumpla la condición.
        //esta instrccuion hacer que 1 se aumente en orden creciente desde el numero 1 hasta llegar al 10 donde la condicion se rompe en el 11
        for(i=1; i<=10;i++)
            System.out.println(i + " ");
    }
}